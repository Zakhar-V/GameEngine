#include "System.hpp"

//---------------------------------------------------------------------------//
// System
//---------------------------------------------------------------------------//

//---------------------------------------------------------------------------//
bool System::Startup(void)
{
	bool _result = false;
	if (!m_initialized)
	{
		_result = _Startup();
		m_initialized = true;
	}
	return _result;
}
//---------------------------------------------------------------------------//
void System::Shutdown(void)
{
	_Shutdown();
	m_initialized = false;
}
